using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Project_Tasks_Class.CircleAreaPerimeter
{
    class AreaPerimeterCircle
    {
        public double cirArea(double radius)
        {
            return 3.14 * radius * radius;
        }
        public double cirPer(double radius)
        {
            return 2 * 3.14 * radius;
        }
    }
}
